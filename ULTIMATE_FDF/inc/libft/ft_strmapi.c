#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*new;
	size_t	newlen;
	size_t	i;

	if (s == NULL || f == NULL)
		return (NULL);
	newlen = ft_strlen(s);
	new = (char *)malloc(sizeof(char) * newlen + 1);
	if (!new)
		return (NULL);
	i = 0;
	while (s[i])
	{
		new[i] = f(i, s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
